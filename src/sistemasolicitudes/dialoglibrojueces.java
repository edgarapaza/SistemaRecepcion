        otorgante = this.txtOtorgante.getText().toUpperCase();
        favorecido = this.txtFavorecido.getText().toUpperCase();
        seguido = this.txtSeguido.getText().toUpperCase();
        demandado = this.txtDemandado.getText().toUpperCase();
        juezpaz = this.txtJuezPaz.getText().toUpperCase();
        lugar = this.txtLugar.getText().toUpperCase();
        dia = this.txtDia.getText().toUpperCase();
        mes = this.cboxMes.getSelectedItem().toString();
        anio = txtAño.getText().toUpperCase();
        nombrebien = this.txtNombreBien.getText().toUpperCase();
        obs = this.txtObservaciones.getText().toUpperCase();
        

