    public void dbcbox(String sql,JComboBox a){
        try {
                con.conectar();
                ResultSet rs = con.consulta(sql);
                while(rs.next()){
                    a.addItem (rs.getObject(1));
                }

        } catch (SQLException e) {
                javax.swing.JOptionPane.showMessageDialog(rootPane, e);
            }
    }
    
