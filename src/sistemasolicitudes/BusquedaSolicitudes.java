    private void Tabla1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Tabla1MouseClicked
        // TODO add your handling code here:
        fila = Tabla1.rowAtPoint(evt.getPoint());
        if(fila > -1){
            String codigo = String.valueOf(Tabla1.getValueAt(fila, 0));
            String nombre = String.valueOf(Tabla1.getValueAt(fila, 1));
            String paterno = String.valueOf(Tabla1.getValueAt(fila, 2));
            String materno = String.valueOf(Tabla1.getValueAt(fila, 3));
            String dni = String.valueOf(Tabla1.getValueAt(fila, 5));
            
            ListaSolicitudes lista = new ListaSolicitudes();
            String[] columNames ={"Numero de Solicitud"};
            String year = this.txtYear.getText();
            
            datSol = lista.getListaSolicitudes(codigo, year);
            DefaultTableModel dat = new DefaultTableModel(datSol,columNames);
            Tabla2.setModel(dat);
            sizeTabla();
            actualizar_Tabla();
        }
        
        activa();
    }//GEN-LAST:event_Tabla1MouseClicked

